    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        int kdPasien = Integer.parseInt(txtFieldPatientCode.getText());
        int harga = Integer.parseInt(txtFieldPrice.getText()) * 2;
        Pembayaran pem = new Pembayaran().getByKdPasien(kdPasien);
        pem.setJumlahHarga(harga);
        pem.save();
        dataToTable();
        JOptionPane.showMessageDialog(this, "Harga telah berhasil diubah");
    }//GEN-LAST:event_UpdateButtonActionPerformed

