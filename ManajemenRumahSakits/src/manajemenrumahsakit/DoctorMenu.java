    private void dataToTable() {
        model.getDataVector().removeAllElements();
        model1.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        model1.fireTableDataChanged();
        
        for (Pasien pasien : new Pasien().getAllByDoctorWait(this.loginKdDokter)) {
            Pembayaran pem = new Pembayaran().getByKdPasien(pasien.getKdPasien());
            Object[] o = new Object[5];
            o[0] = String.valueOf(pasien.getKdPasien());
            o[1] = pasien.getNamaPasien();
            o[2] = pasien.getAlamatPasien();
            o[3] = pasien.getKeluhan();
            o[4] = pem.getJumlahHarga();
            model.addRow(o);
        }
        
        for (Pasien pasien : new Pasien().getAllByDoctorDone(this.loginKdDokter)) {
            Pembayaran pem = new Pembayaran().getByKdPasien(pasien.getKdPasien());
            Object[] o = new Object[5];
            o[0] = String.valueOf(pasien.getKdPasien());
            o[1] = pasien.getNamaPasien();
            o[2] = pasien.getAlamatPasien();
            o[3] = pasien.getKeluhan();
            o[4] = pem.getJumlahHarga();
            model1.addRow(o);
        }
    }

