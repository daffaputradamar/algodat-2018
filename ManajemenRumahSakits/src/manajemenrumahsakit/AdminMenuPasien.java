    private void inCBSpesializationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inCBSpesializationActionPerformed
        String spe = inCBSpesialization.getSelectedItem().toString();
        Spesialisasi spesial = new Spesialisasi().getByName(spe);
        inCBDoctor.setModel(new DefaultComboBoxModel(
                new Dokter().getBySpesialisasi(spesial.getKdSpesialisasi()).toArray()
        ));
    }//GEN-LAST:event_inCBSpesializationActionPerformed

    private void tblPatientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPatientMouseClicked
        int selectedRow = tblPatient.getSelectedRow();
        String patientCode = model.getValueAt(selectedRow, 0).toString();
        txtFieldPatientCodeUpdate.setText(patientCode);
        txtFieldPatientCodeDelete.setText(patientCode);
        int doctorCode = Integer.parseInt(model.getValueAt(selectedRow, 5).toString());
        Dokter doc = new Dokter().getById(doctorCode);
        int speCode = doc.getKdSpesialisasi();
        jCBDokter.setModel(new DefaultComboBoxModel(
                new Dokter().getBySpesialisasi(speCode).toArray()
        ));
    }//GEN-LAST:event_tblPatientMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        Pasien pas = new Pasien().getById(Integer.parseInt(txtFieldPatientCodeUpdate.getText()));
        Dokter doc = new Dokter().getByName(jCBDokter.getSelectedItem().toString());
        pas.setKdDokter(doc.getKdDokter());
        pas.save();
        JOptionPane.showMessageDialog(this, "Data telah terupdate");
        dataToTable();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        Pasien pas = new Pasien().getById(Integer.parseInt(txtFieldPatientCodeDelete.getText()));
        pas.delete();
        JOptionPane.showMessageDialog(this, "Data telah terhapus");
        dataToTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Pasien pas = new Pasien();
        pas.setNamaPasien(inName.getText());
        pas.setAlamatPasien(inAlamat.getText());
        pas.setKeluhan(inComplaint.getText());
        Dokter dok = new Dokter().getByName(inCBDoctor.getSelectedItem().toString());
        pas.setKdDokter(dok.getKdDokter());
        pas.save();
        JOptionPane.showMessageDialog(this, "Data telah ditambah");
        dataToTable();
    }//GEN-LAST:event_btnAddActionPerformed

