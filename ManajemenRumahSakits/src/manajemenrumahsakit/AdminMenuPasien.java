    private void dataToTable() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        for (Pasien pasien : new Pasien().getAll()) {
            Object[] o = new Object[7];
            o[0] = String.valueOf(pasien.getKdPasien());
            o[1] = pasien.getNamaPasien();
            o[2] = pasien.getAlamatPasien();
            o[3] = pasien.getTglDatang();
            o[4] = pasien.getKeluhan();
            o[5] = String.valueOf(pasien.getDokter().getKdDokter());
            o[6] = pasien.getDokter().getNamaDokter();
            model.addRow(o);
        }
    }

